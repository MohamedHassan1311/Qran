import 'dart:convert';

Date dataFromJson(String str) => Date.fromJson(json.decode(str));

String dataToJson(Date data) => json.encode(data.toJson());

class Date {
  Date({
    this.code,
    this.status,
    this.data,
  });

  int code;
  String status;
  DataClass data;

  factory Date.fromJson(Map<String, dynamic> json) => Date(
        code: json["code"],
        status: json["status"],
        data: DataClass.fromJson(json["data"]),
      );

  Map<String, dynamic> toJson() => {
        "code": code,
        "status": status,
        "data": data.toJson(),
      };
}

class DataClass {
  DataClass({
    this.hijri,
    this.gregorian,
  });

  Hijri hijri;
  Gregorian gregorian;

  factory DataClass.fromJson(Map<String, dynamic> json) => DataClass(
        hijri: Hijri.fromJson(json["hijri"]),
        gregorian: Gregorian.fromJson(json["gregorian"]),
      );

  Map<String, dynamic> toJson() => {
        "hijri": hijri.toJson(),
        "gregorian": gregorian.toJson(),
      };
}

